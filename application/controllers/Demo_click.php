<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Demo_click extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Demo_click_model');
        $this->load->library('form_validation');        
	$this->load->library('datatables');
    }

    public function index()
    {
        $this->load->view('demo_click/demo_click_list');
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Demo_click_model->json();
    }

    public function read($id) 
    {
        $row = $this->Demo_click_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'numberofclick' => $row->numberofclick,
		'created_at' => $row->created_at,
	    );
            $this->load->view('demo_click/demo_click_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('demo_click'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('demo_click/create_action'),
	    'id' => set_value('id'),
	    'numberofclick' => set_value('numberofclick'),
	    'created_at' => set_value('created_at'),
	);
        $this->load->view('demo_click/demo_click_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'numberofclick' => $this->input->post('numberofclick',TRUE),
		'created_at' => $this->input->post('created_at',TRUE),
	    );

            $this->Demo_click_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('demo_click'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Demo_click_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('demo_click/update_action'),
		'id' => set_value('id', $row->id),
		'numberofclick' => set_value('numberofclick', $row->numberofclick),
		'created_at' => set_value('created_at', $row->created_at),
	    );
            $this->load->view('demo_click/demo_click_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('demo_click'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'numberofclick' => $this->input->post('numberofclick',TRUE),
		'created_at' => $this->input->post('created_at',TRUE),
	    );

            $this->Demo_click_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('demo_click'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Demo_click_model->get_by_id($id);

        if ($row) {
            $this->Demo_click_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('demo_click'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('demo_click'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('numberofclick', 'numberofclick', 'trim|required');
	$this->form_validation->set_rules('created_at', 'created at', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

